import LayoutProvider from "@/components/layout/layout.component";
import { ToastContainer } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";
import type { Metadata } from "next";
import { cookies } from "next/headers";
import AppProvider from "../providers/app-provider";
import AutoChangeToken from "@/components/auto-change-token.component";
import UserProvider from "@/providers/user-provider";
import BlogProvider from "@/providers/blog-provider";
import { I_User } from "@/interfaces/user.interface";

export const metadata: Metadata = {
    title: "Create Next App",
    description: "Generated by create next app",
};

export default function RootLayout({
    children,
}: Readonly<{
    children: React.ReactNode;
}>) {
    let userInfo;
    const cookieStore = cookies();
    const access_token = cookieStore.get("access_token")?.value;
    const refresh_token = cookieStore.get("refresh_token")?.value;
    const userCookie = cookieStore.get("user");
    if (userCookie) {
        userInfo = JSON.parse(userCookie?.value);
    }

    return (
        <html lang="en">
            <body>
                <AppProvider access_token={access_token} refresh_token={refresh_token}>
                    <UserProvider userInfo={userInfo}>
                        <BlogProvider>
                            <LayoutProvider>
                                {children}
                                <AutoChangeToken />
                            </LayoutProvider>
                        </BlogProvider>
                    </UserProvider>
                </AppProvider>
                <ToastContainer />
            </body>
        </html>
    );
}
